#include <stdio.h>
int main(){

    printf("I love pizza\n"); // \n is a new line escape sequence
    printf("\"One man can change the world.\" Abraham L."); // \" Double quotes

    /* 
    \n -    New line 
    \" -    Double quotes
    \' -    Single quote
    \\ -    Backslash
    \t -    Tab
    \b -    Backspace
    
    */

    return 0;
}